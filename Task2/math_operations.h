#include <stdio.h>

int add(int a, int b){
    int result;
    result = a + b;
    return result;
}
int subtract(int a, int b){
    int result;
    result = a - b;
    return result;
}
int divide(int a, int b){
    int result;
    result = a / b;
    return result;
}
int multiply(int a, int b){
    int result;
    result = a * b;
    return result;
}
